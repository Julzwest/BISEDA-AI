export const getRandomEnglishSlang = (category) => {
  const slang = ENGLISH_SLANG[category] || ENGLISH_SLANG.genZ.reactions;
  return slang[Math.floor(Math.random() * slang.length)];
};

export const getRandomAlbanianSlang = (category) => {
  const slang = ALBANIAN_SLANG[category] || ALBANIAN_SLANG.reactions;
  return slang[Math.floor(Math.random() * slang.length)];
};

export const getAllEnglishTerms = () => {
  let terms = [];
  Object.keys(ENGLISH_SLANG).forEach(category => {
    if (Array.isArray(ENGLISH_SLANG[category])) {
      terms = terms.concat(ENGLISH_SLANG[category]);
    } else {
      Object.keys(ENGLISH_SLANG[category]).forEach(subcat => {
        terms = terms.concat(ENGLISH_SLANG[category][subcat]);
      });
    }
  });
  return terms;
};

export const getAllAlbanianTerms = () => {
  let terms = [];
  Object.keys(ALBANIAN_SLANG).forEach(category => {
    terms = terms.concat(ALBANIAN_SLANG[category]);
  });
  return terms;
};

// Count total terms
export const getSlangStats = () => {
  const englishTerms = getAllEnglishTerms();
  const albanianTerms = getAllAlbanianTerms();
  return {
    english: englishTerms.length,
    albanian: albanianTerms.length,
    total: englishTerms.length + albanianTerms.length
  };
};

export default { ENGLISH_SLANG, ALBANIAN_SLANG, getSlangStats };

